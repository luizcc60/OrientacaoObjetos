package construtoraVeiculos11;

public class Cidade {

	// ATRIBUTOS
	private int codigo;
	private String nome;
	private UF uf;

	// GETTERS E SETTERS
	public int getCodigo() {
		return codigo;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public UF getUf() {
		return uf;
	}

	public void setUf(UF uf) {
		this.uf = uf;
	}

}
